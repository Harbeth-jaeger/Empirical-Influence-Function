# Output
# --------------------------------------------------------------------------- #
def write_jsonl(path: Path, rows: Iterable[dict[str, Any]]) -> int:
    path.parent.mkdir(parents=True, exist_ok=True)
    n = 0
    with path.open("w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")
            n += 1
    return n


def build_language(lang: str, raw_root: Path, split: str, cap: int, max_source_rows: int,
                   seed: int, workers: int = 1):
    sources = discover_sources(raw_root, lang, split)
    stats = BuildStats()
    samples: list[dict[str, Any]] = []
    seen: set[str] = set()

    def absorb(keyed_samples, sub_stats):
        stats.files_seen += 1
        merge_stats(stats, sub_stats)
        for key, sample in keyed_samples:
            if key in seen:
                stats.reject("duplicate_normalized")
                continue
            seen.add(key)
            samples.append(sample)
            stats.accept()

    tasks = [(src, lang, max_source_rows, split) for src in sources]

    if workers <= 1 or len(sources) <= 1:
        for task in tasks:
            keyed_samples, sub_stats, label = process_one_source(task)
            absorb(keyed_samples, sub_stats)
            print(f"[{lang}] done {label} "
                  f"(files {stats.files_seen}/{len(sources)}, accepted={stats.rows_accepted})",
                  flush=True)
    else:
        with ProcessPoolExecutor(max_workers=workers) as ex:
            for keyed_samples, sub_stats, label in ex.map(process_one_source, tasks):
                absorb(keyed_samples, sub_stats)
                print(f"[{lang}] done {label} "
                      f"(files {stats.files_seen}/{len(sources)}, accepted={stats.rows_accepted})",
                      flush=True)

    return select_balanced(samples, cap, seed), stats, [s.label() for s in sources]


def write_report(path: Path, jobs: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = ["# CodeSearchNet Multi-Language Single-Statement Data Report", ""]
    for job in jobs:
        lang, split, rows, stats = job["lang"], job["split"], job["rows"], job["stats"]
        sources = job["sources"]
        lines += [f"## {LANG_DISPLAY[lang]} / {lang}_single / {split}", ""]
        lines += [
            f"- sources: {len(sources)}",
            f"- files_seen: {stats.files_seen}",
            f"- rows_seen: {stats.rows_seen}",
            f"- candidates_seen: {stats.candidates_seen}",
            f"- accepted_before_sampling: {stats.rows_accepted}",
            f"- final_samples: {len(rows)}",
            "",
        ]
        lines.append("### target_kind")
        for kind, count in Counter(r.get("target_kind", "unknown") for r in rows).most_common():
            lines.append(f"- {kind}: {count}")
        lines += ["", "### reject_reasons"]
        for reason, count in stats.reject_reasons.most_common(30):
            lines.append(f"- {reason}: {count}")
        lines.append("")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_preview(path: Path, jobs: list[dict[str, Any]], seed: int, limit: int) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    rng = random.Random(seed)
    fences = {
        "python": "python", "java": "java", "c": "c", "cpp": "cpp",
        "csharp": "csharp", "javascript": "javascript",
        "go": "go", "php": "php", "ruby": "ruby",
    }
    lines = ["# CodeSearchNet Multi-Language Sample Preview", ""]
    for job in jobs:
        lang, split, rows = job["lang"], job["split"], job["rows"]
        sample_rows = list(rows)
        rng.shuffle(sample_rows)
        sample_rows = sample_rows[:limit]
        lines += [f"## {LANG_DISPLAY[lang]} / {split}", ""]
        for i, sample in enumerate(sample_rows, 1):
            lines += [
                f"### {i}. {sample['uid']} ({sample.get('target_kind')})",
                "",
                f"source_dataset: `{sample.get('source_dataset')}`",
                "",
                f"```{fences[lang]}",
                sample["prefix"] + "[MASK]" + sample["suffix"],
                "```",
                "",
                "target:",
                f"```{fences[lang]}",
                sample["target"],
                "```",
                "",
            ]
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


