@dataclass(frozen=True)
class RecordSource:
    kind: str
    path: Path
    member: str | None = None

    def label(self) -> str:
        return str(self.path) if self.member is None else f"{self.path}!{self.member}"


@dataclass(frozen=True)
class CandidateSpan:
    start: int
    end: int
    target: str
    target_kind: str
    line_no: int


@dataclass
class BuildStats:
    files_seen: int = 0
    rows_seen: int = 0
    candidates_seen: int = 0
    rows_accepted: int = 0
    reject_reasons: Counter[str] = field(default_factory=Counter)

    def reject(self, reason: str) -> None:
        self.reject_reasons[reason] += 1

    def accept(self) -> None:
        self.rows_accepted += 1


def merge_stats(dst: BuildStats, src: BuildStats) -> None:
    """Fold one BuildStats into another (used when combining worker results)."""
    dst.rows_seen += src.rows_seen
    dst.candidates_seen += src.candidates_seen
    dst.reject_reasons.update(src.reject_reasons)


# --------------------------------------------------------------------------- #
# Small helpers
# --------------------------------------------------------------------------- #
def stable_hash(text: str, n: int = 16) -> str:
    return hashlib.sha1(text.encode("utf-8", errors="ignore")).hexdigest()[:n]


def normalize_code(code: str) -> str:
    """Normalise string/number literals and whitespace for dedup comparison."""
    code = re.sub(r'"(?:[^"\\]|\\.)*"', '"STR"', code)
    code = re.sub(r"'(?:[^'\\]|\\.)*'", "'STR'", code)
    code = re.sub(r"\b\d+(?:\.\d+)?\b", "NUM", code)
    return re.sub(r"\s+", "", code)


def count_nonempty_lines(code: str) -> int:
    return sum(1 for line in code.splitlines() if line.strip())


def parse_languages(value: str) -> list[str]:
    out: list[str] = []
    for raw in value.split(","):
        key = raw.strip().lower()
        if not key:
            continue
        if key not in LANG_ALIASES:
            raise ValueError(f"unsupported language {raw!r}")
        lang = LANG_ALIASES[key]
        if lang not in out:
            out.append(lang)
    return out


