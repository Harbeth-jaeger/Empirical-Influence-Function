def parse_args():
    p = argparse.ArgumentParser(
        description="Build CodeSearchNet single-statement completion data for multiple languages."
    )
    p.add_argument("--raw_root", type=Path, default=Path("data/"))
    p.add_argument("--output_root", type=Path, default=Path("data"))
    p.add_argument("--languages", default="go,java,javascript,php,python,ruby")
    p.add_argument(
        "--splits",
        default="train,valid,test",
        help="CSN splits to process. Only `train` is subsampled to --per_language; "
             "valid/test are emitted in full (no subsampling).",
    )
    p.add_argument(
        "--per_language",
        type=int,
        default=0,
        help="Subsample the train split to N balanced rows. 0 (default) = no "
             "subsampling (use all). valid/test are always emitted in full.",
    )
    p.add_argument(
        "--max_source_rows",
        type=int,
        default=0,
        help="Optional cap per source file before filtering; 0 means all rows. "
             "Note: with --workers > 1 this is per-file, not global.",
    )
    p.add_argument("--seed", type=int, default=42)
    p.add_argument(
        "--workers",
        type=int,
        default=1,
        help="Parallel worker processes per language (one source file per task).",
    )
    p.add_argument("--preview_per_lang", type=int, default=20)
    p.add_argument(
        "--report_path",
        type=Path,
        default=Path("outputs/docs/codesearchnet_multilang_single_report.md"),
    )
    p.add_argument(
        "--preview_path",
        type=Path,
        default=Path("outputs/docs/codesearchnet_multilang_single_preview.md"),
    )
    return p.parse_args()


def main():
    args = parse_args()
    langs = parse_languages(args.languages)
    splits = [s.strip() for s in args.splits.split(",") if s.strip()]
    jobs: list[dict[str, Any]] = []

    for lang in langs:
        for split in splits:
            # Only train is subsampled; valid/test are emitted in full.
            cap = args.per_language if split == "train" else 0
            rows, stats, sources = build_language(
                lang, args.raw_root, split, cap, args.max_source_rows, args.seed, args.workers
            )
            if not sources:
                print(f"[warn] no {split} sources found for {lang} under {args.raw_root}")
                continue

            out_dir = args.output_root / f"{lang}_single" / "train_data"
            canonical_path = out_dir / f"{lang}_single_{split}_codesearchnet_{len(rows)}_canonical.jsonl"
            chatml_path = out_dir / f"{lang}_single_{split}_codesearchnet_{len(rows)}_chatml.jsonl"
            write_jsonl(canonical_path, rows)
            write_jsonl(chatml_path, (render_chatml(r) for r in rows))
            print(
                f"[{lang}/{split}] sources={len(sources)} rows_seen={stats.rows_seen} "
                f"accepted_before_sampling={stats.rows_accepted} final={len(rows)} "
                f"{'(subsampled)' if cap else '(all)'} chatml={chatml_path}"
            )
            jobs.append({"lang": lang, "split": split, "rows": rows, "stats": stats, "sources": sources})

    write_report(args.report_path, jobs)
    write_preview(args.preview_path, jobs, args.seed, args.preview_per_lang)
    print(f"report={args.report_path}")
    print(f"preview={args.preview_path}")


if __name__ == "__main__":
    main()