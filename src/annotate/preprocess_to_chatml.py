# Source discovery and record iteration
# --------------------------------------------------------------------------- #
def _source_in_split(src: RecordSource, split: str) -> bool:
    """True if a source file belongs to the given CSN split (train/valid/test).

    Matches the split as a path component (e.g. .../final/jsonl/train/...) or in
    the file name (e.g. go_train_0.jsonl.gz)."""
    ref = src.member if src.member else str(src.path)
    p = Path(ref)
    name = p.name.lower()
    return (split in [part.lower() for part in p.parts]
            or f"_{split}" in name or name.startswith(f"{split}_") or name.startswith(f"{split}."))


def discover_sources(root: Path, lang: str, split: str | None = None) -> list[RecordSource]:
    sources: list[RecordSource] = []
    names = [lang]
    if lang == "cpp":
        names += ["c++", "cplusplus"]
    if lang == "csharp":
        names += ["c#", "cs"]
    if lang == "javascript":
        names += ["js", "node", "nodejs"]
    if lang == "go":
        names += ["golang"]
    if lang == "ruby":
        names += ["rb"]

    found_subdir = False
    for name in names:
        base = root / name
        if base.exists():
            found_subdir = True
            sources += [RecordSource("jsonl", p) for p in sorted(base.rglob("*.jsonl"))]
            sources += [RecordSource("jsonl_gz", p) for p in sorted(base.rglob("*.jsonl.gz"))]
        archive = root / f"{name}.zip"
        if archive.exists():
            found_subdir = True
            with zipfile.ZipFile(archive) as zf:
                for member in sorted(zf.namelist()):
                    if member.endswith(".jsonl") or member.endswith(".jsonl.gz"):
                        sources.append(RecordSource("zip", archive, member))

    # Fallback: raw_root itself directly contains the *.jsonl[.gz] files,
    # e.g. --raw_root data/java/final/jsonl/train holding java_train_*.jsonl.gz
    # with no per-language subdirectory.
    if not found_subdir:
        sources += [RecordSource("jsonl", p) for p in sorted(root.rglob("*.jsonl"))]
        sources += [RecordSource("jsonl_gz", p) for p in sorted(root.rglob("*.jsonl.gz"))]

    if split:
        sources = [s for s in sources if _source_in_split(s, split)]

    return sources


def iter_json_lines_from_source(src: RecordSource) -> Iterator[str]:
    if src.kind == "jsonl":
        with src.path.open("rt", encoding="utf-8", errors="ignore") as f:
            yield from f
    elif src.kind == "jsonl_gz":
        with gzip.open(src.path, "rt", encoding="utf-8", errors="ignore") as f:
            yield from f
    elif src.kind == "zip":
        assert src.member is not None
        with zipfile.ZipFile(src.path) as zf:
            with zf.open(src.member) as raw:
                if src.member.endswith(".gz"):
                    with gzip.open(raw, "rt", encoding="utf-8", errors="ignore") as f:
                        yield from f
                else:
                    for b in raw:
                        yield b.decode("utf-8", errors="ignore")


def iter_records(src: RecordSource) -> Iterator[dict[str, Any]]:
    for line in iter_json_lines_from_source(src):
        line = line.strip()
        if not line:
            continue
        try:
            obj = json.loads(line)
        except json.JSONDecodeError:
            continue
        if isinstance(obj, dict):
            yield obj


# --------------------------------------------------------------------------- #
# Row-level filtering
# --------------------------------------------------------------------------- #
def get_code(row: dict[str, Any]) -> str:
    for key in ("code", "original_string", "func_code_string", "function", "content"):
        val = row.get(key)
        if isinstance(val, str) and val.strip():
            return val
    return ""


def split_line_comment(text: str, lang: str) -> tuple[str, bool]:
    """Return (code_without_trailing_comment, had_comment).

    Scans char-by-char so comment markers inside string/char literals are not
    mistaken for comments. Handles `#` for Python and `//` / `/*` for C-like
    and JavaScript. The code part is right-stripped; anything from the comment
    marker onward is dropped.
    """
    in_string: str | None = None
    escaped = False
    quotes = {'"', "'", "`"} if lang in ("javascript", "go") else {'"', "'"}
    hash_comment = lang in ("python", "ruby", "php")
    slash_comment = lang in ("c", "cpp", "csharp", "java", "javascript", "go", "php")
    for i, ch in enumerate(text):
        if in_string:
            if escaped:
                escaped = False
            elif ch == "\\":
                escaped = True
            elif ch == in_string:
                in_string = None
            continue
        if ch in quotes:
            in_string = ch
            continue
        if hash_comment and ch == "#":
            return text[:i].rstrip(), True
        if slash_comment and ch == "/" and i + 1 < len(text) and text[i + 1] in "/*":
            return text[:i].rstrip(), True
    return text, False


def should_reject_row(row: dict[str, Any], lang: str) -> str | None:
    path = str(row.get("path") or row.get("filepath") or row.get("file_path") or "")
    code = get_code(row)
    func_name = str(row.get("func_name") or row.get("function_name") or "")

    if not code.strip():
        return "missing_code"
    suffix = Path(path).suffix.lower() if path else ""
    if suffix and suffix not in LANG_EXTS[lang]:
        return "wrong_extension"
    if TEST_PATH_RE.search(path.replace("\\", "/")):
        return "test_path"
    if GENERATED_RE.search(code):
        return "generated_code"
    return None


# --------------------------------------------------------------------------- #
# Line / body geometry
# --------------------------------------------------------------------------- #
def line_offsets(code: str) -> Iterator[tuple[int, int, int, str]]:
    """Yield (line_no, start, end, text) where [start, end) covers the line
    content (excluding the trailing newline)."""
    offset = 0
    for line_no, raw in enumerate(code.splitlines(keepends=True), start=1):
        start = offset
        offset += len(raw)
        text = raw[:-1] if raw.endswith("\n") else raw
        if text.endswith("\r"):
            text = text[:-1]
        yield line_no, start, start + len(text), text


def py_body_start(code: str) -> int:
    """Return the offset where the function body begins, just after the
    `def ...:` header. Handles signatures that wrap across multiple lines."""
    rows = list(line_offsets(code))
    for i, (_, _, _, line) in enumerate(rows):
        stripped = line.strip()
        if not (stripped.startswith("def ") or stripped.startswith("async def ")):
            continue
        # Consume header lines until the parentheses are balanced and the line
        # closes the header with ':'.
        depth = 0
        for j in range(i, len(rows)):
            text = rows[j][3]
            depth += text.count("(") - text.count(")")
            if depth <= 0 and text.rstrip().endswith(":"):
                return rows[j + 1][1] if j + 1 < len(rows) else len(code)
        return len(code)
    return 0


def outer_brace_body(code: str) -> tuple[int, int] | None:
    """Return [start, end) of the outermost `{ ... }` body, skipping braces that
    appear inside string/char literals."""
    open_pos = code.find("{")
    if open_pos < 0:
        return None
    depth = 0
    in_string: str | None = None
    escaped = False
    for i in range(open_pos, len(code)):
        ch = code[i]
        if in_string:
            if escaped:
                escaped = False
            elif ch == "\\":
                escaped = True
            elif ch == in_string:
                in_string = None
            continue
        if ch in {'"', "'", "`"}:
            in_string = ch
            continue
        if ch == "{":
            depth += 1
        elif ch == "}":
            depth -= 1
            if depth == 0:
                return open_pos + 1, i
    return None


# --------------------------------------------------------------------------- #
# Statement classification
# --------------------------------------------------------------------------- #
def _looks_like_assignment(expr: str) -> str | None:
    """Return the LHS identifier if `expr` is a real assignment, else None."""
    if not ASSIGN_RE.search(expr):
        return None
    if any(op in expr for op in ("==", "!=", "<=", ">=")):
        return None
    lhs = ASSIGN_SPLIT_RE.split(expr, maxsplit=1)[0].strip()
    if lhs and lhs != "_":
        return lhs
    return None


def classify_target(line: str, lang: str) -> str | None:
    stripped = line.strip()
    if not stripped:
        return None
    lowered = stripped.lower()

    if lang == "python":
        if stripped.endswith(":") or any(lowered.startswith(p) for p in PY_BLOCK_PREFIXES):
            return None
        if "{" in stripped or "}" in stripped or ";" in stripped:
            return None
        if stripped.startswith("return"):
            if not re.match(r"^return\s+\S", stripped):
                return None
            if lowered in LOW_INFO_RETURNS:
                return None
            return "return"
        if _looks_like_assignment(stripped):
            return "assignment"
        if CALL_RE_PY.match(stripped):
            return "call"
        return None

    if lang in ("javascript", "go"):
        # Brace-based, but the trailing semicolon is optional (JS ASI / Go lexer).
        blocks = JS_BLOCK_PREFIXES if lang == "javascript" else GO_BLOCK_PREFIXES
        if "{" in stripped or "}" in stripped:
            return None
        if any(lowered.startswith(p) for p in blocks):
            return None
        core = stripped[:-1].strip() if stripped.endswith(";") else stripped
        if not core:
            return None
        if core.startswith("return"):
            if not re.match(r"^return\s+\S", core):
                return None
            if core.lower() in LOW_INFO_RETURNS:
                return None
            return "return"
        if _looks_like_assignment(core):
            return "assignment"
        if CALL_RE_JS.match(core):
            return "call"
        return None

    if lang == "php":
        if any(lowered.startswith(p) for p in PHP_BLOCK_PREFIXES):
            return None
        if "{" in stripped or "}" in stripped or not stripped.endswith(";"):
            return None
        core = stripped[:-1].strip()
        if core.startswith("return"):
            if not re.match(r"^return\s+\S", core):
                return None
            if core.lower() in LOW_INFO_RETURNS:
                return None
            return "return"
        if _looks_like_assignment(core):
            return "assignment"
        if CALL_RE_PHP.match(stripped):
            return "call"
        return None

    if lang == "ruby":
        # def...end blocks, no braces, no semicolons.
        if any(lowered.startswith(p) for p in RUBY_BLOCK_PREFIXES):
            return None
        if stripped.endswith(("do", "|", "{", "}", "=begin", "=end")):
            return None
        core = stripped
        if core.startswith("return"):
            if not re.match(r"^return\s+\S", core):
                return None
            if core.lower() in LOW_INFO_RETURNS:
                return None
            return "return"
        if _looks_like_assignment(core):
            return "assignment"
        if CALL_RE_RUBY.match(core):
            return "call"
        return None

    # C-like languages
    if any(lowered.startswith(p) for p in CLIKE_BLOCK_PREFIXES):
        return None
    if "{" in stripped or "}" in stripped or not stripped.endswith(";"):
        return None
    core = stripped[:-1].strip()
    lowered_core = core.lower()
    if core.startswith("return"):
        if not re.match(r"^return\s+\S", core):
            return None
        if lowered_core in LOW_INFO_RETURNS or lowered in LOW_INFO_RETURNS:
            return None
        return "return"
    if _looks_like_assignment(core):
        return "assignment"
    if CALL_RE_CLIKE.match(stripped):
        return "call"
    return None


def extract_statement_candidates(function_code: str, lang: str) -> tuple[list[CandidateSpan], str | None]:
    if lang == "python":
        body_start, body_end = py_body_start(function_code), len(function_code)
    else:
        bounds = outer_brace_body(function_code)
        body_start, body_end = bounds if bounds is not None else (0, len(function_code))

    body = function_code[body_start:body_end]
    line_count = count_nonempty_lines(body)
    if line_count < 2:
        return [], "function_too_short"
    if line_count > 150:
        return [], "function_too_long"

    out: list[CandidateSpan] = []
    for line_no, start, end, text in line_offsets(function_code):
        if end <= body_start or start >= body_end:
            continue
        # Noise (print/log) or test symbols (assert/Test) on this line -> try
        # the next line instead.
        if NOISY_RE.search(text) or TEST_SYMBOL_RE.search(text):
            continue
        # Trailing comment -> drop the comment, keep the code as the target.
        # Shrink the span so the comment text stays in the suffix and the full
        # code still reconstructs.
        code_text, had_comment = split_line_comment(text, lang)
        if had_comment:
            end = start + len(code_text)
            text = code_text
        if not text.strip():
            continue
        kind = classify_target(text, lang)
        if not kind:
            continue
        tokens = len(re.findall(r"[A-Za-z_]\w*|\S", text.strip()))
        if tokens < 3 or tokens > 60:
            continue
        out.append(CandidateSpan(start, end, text, kind, line_no))
    return out, None


# --------------------------------------------------------------------------- #
# Sample construction
# --------------------------------------------------------------------------- #
def make_canonical_sample(
    lang: str,
    row: dict[str, Any],
    source_label: str,
    cand: CandidateSpan,
    function_code: str,
    split: str = "train",
) -> dict[str, Any]:
    prefix = function_code[: cand.start]
    suffix = function_code[cand.end:]
    target = cand.target
    uid_base = "\n".join([
        str(row.get("repo", "")),
        str(row.get("path", "")),
        str(row.get("func_name", "")),
        source_label,
        str(cand.line_no),
        target,
    ])
    uid = f"codesearchnet_{lang}_single_{stable_hash(uid_base)}"
    return {
        "uid": uid,
        "source_dataset": f"codesearchnet_{lang}",
        "split": split,
        "language": LANG_DISPLAY[lang],
        "task_type": f"{lang}_single_statement_completion",
        "prefix": prefix,
        "target": target,
        "suffix": suffix,
        "full_code": prefix + target + suffix,
        "target_kind": cand.target_kind,
        "metadata": {
            "repo": row.get("repo"),
            "source_path": row.get("path"),
            "function_name": row.get("func_name"),
            "sha": row.get("sha"),
            "url": row.get("url"),
            "raw_source": source_label,
            "target_line": cand.line_no,
            "target_span": [cand.start, cand.end],
            "function_line_count": count_nonempty_lines(function_code),
            "filters": ["no_comments", "single_line", cand.target_kind],
        },
        "judge_payload": {"kind": "none"},
    }


def render_chatml(sample: dict[str, Any]) -> dict[str, Any]:
    lang_name = str(sample["language"])
    user = (
        f"Fill the [MASK] in the {lang_name} function. Return only the missing "
        f"{lang_name} code, without Markdown fences or explanation.\n\n"
        "* Incomplete Code:\n"
        f"{sample['prefix']}[MASK]{sample['suffix']}"
    )
    out = dict(sample)
    out["messages"] = [
        {"role": "system", "content": f"You are a {lang_name} code completion assistant."},
        {"role": "user", "content": user},
        {"role": "assistant", "content": sample["target"]},
    ]
    out["only_last_turn_loss"] = True
    return out


def select_balanced(samples: list[dict[str, Any]], n: int, seed: int) -> list[dict[str, Any]]:
    rng = random.Random(seed)
    if n <= 0 or len(samples) <= n:
        out = list(samples)
        rng.shuffle(out)
        return out

    by_kind: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for s in samples:
        by_kind[s.get("target_kind", "unknown")].append(s)

    selected: list[dict[str, Any]] = []
    used: set[str] = set()
    for kind, ratio in TARGET_RATIOS.items():
        quota = int(round(n * ratio))
        bucket = by_kind.get(kind, [])
        rng.shuffle(bucket)
        for s in bucket[:quota]:
            selected.append(s)
            used.add(s["uid"])

    if len(selected) < n:
        rest = [s for s in samples if s["uid"] not in used]
        rng.shuffle(rest)
        selected.extend(rest[: n - len(selected)])

    rng.shuffle(selected)
    return selected[:n]


# --------------------------------------------------------------------------- #
# Per-source worker (parallel-safe)
# --------------------------------------------------------------------------- #
def process_one_source(task: tuple[RecordSource, str, int]):
    """Process a single source file in isolation.

    Runs in a worker process. Dedup is NOT done here, because the `seen` set
    cannot be shared across processes; instead each accepted sample is returned
    together with its dedup key, and the parent deduplicates globally.

    Returns (list[(key, sample)], BuildStats, source_label).
    """
    src, lang, max_source_rows, split = task
    stats = BuildStats()
    stats.files_seen = 1
    keyed_samples: list[tuple[str, dict[str, Any]]] = []

    for row in iter_records(src):
        if max_source_rows > 0 and stats.rows_seen >= max_source_rows:
            break
        stats.rows_seen += 1
        reason = should_reject_row(row, lang)
        if reason:
            stats.reject(reason)
            continue
        function_code = get_code(row)
        cands, reason = extract_statement_candidates(function_code, lang)
        if reason:
            stats.reject(reason)
            continue
        for cand in cands:
            stats.candidates_seen += 1
            key = stable_hash(normalize_code(function_code) + "\n" + normalize_code(cand.target))
            keyed_samples.append((key, make_canonical_sample(lang, row, src.label(), cand, function_code, split)))

    return keyed_samples, stats, src.label()


