"""Build CodeSearchNet single-statement completion data for multiple languages.

The pipeline reads raw CodeSearchNet function records, filters out unsuitable
functions (tests, generated code, commented code, noisy I/O, ...), extracts a
single "fill-in" statement per candidate (assignment / return / call), balances
the target kinds, and writes canonical + ChatML JSONL plus a report/preview.
"""
