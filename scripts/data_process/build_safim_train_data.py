@dataclass(frozen=True)
class SpanCandidate:
    task_type: str
    start: int
    end: int
    target_kind: str
    score: tuple[int, int, int]


def read_jsonl(path: Path) -> Iterator[dict[str, Any]]:
    if not path.exists():
        return
    with path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                yield json.loads(line)


def write_jsonl(path: Path, rows: Iterable[dict[str, Any]]) -> int:
    path.parent.mkdir(parents=True, exist_ok=True)
    n = 0
    with path.open("w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")
            n += 1
    return n


def write_json(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def stable_hash(text: str) -> str:
    return hashlib.sha1(text.encode("utf-8", errors="replace")).hexdigest()


def normalize_code(text: str) -> str:
    text = text.replace("\r\n", "\n").replace("\r", "\n")
    text = re.sub(r"[ \t]+", " ", text)
    text = re.sub(r"\n+", "\n", text)
    return text.strip()


def line_offsets(text: str) -> list[int]:
    offsets = [0]
    for idx, ch in enumerate(text):
        if ch == "\n":
            offsets.append(idx + 1)
    return offsets


def span_from_lines(text: str, start_line: int, end_line: int) -> tuple[int, int]:
    offsets = line_offsets(text)
    start_idx = max(0, min(start_line - 1, len(offsets) - 1))
    end_idx = max(start_idx, min(end_line, len(offsets)))
    start = offsets[start_idx]
    end = offsets[end_idx] if end_idx < len(offsets) else len(text)
    return start, end


def slice_span(text: str, start: int, end: int) -> tuple[str, str, str]:
    return text[:start], text[start:end], text[end:]


def line_col_to_offset(offsets: list[int], lineno: int, col: int) -> int:
    return offsets[lineno - 1] + col


