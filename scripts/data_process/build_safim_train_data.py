def load_stack_stream(language: str, token: str | bool | None) -> Iterable[dict[str, Any]]:
    try:
        from datasets import load_dataset
    except ImportError as exc:
        raise RuntimeError(
            "Missing dependency 'datasets'. Activate the eif-bench environment or install requirements.txt."
        ) from exc
    return load_dataset(
        "bigcode/the-stack",
        data_dir=LANGUAGES[language]["data_dir"],
        split="train",
        streaming=True,
        token=token,
    )


def build_language(language: str, args: argparse.Namespace, full_decontam: set[str], target_decontam: set[str]) -> tuple[list[dict[str, Any]], list[dict[str, Any]], dict[str, Any]]:
    rng = random.Random(args.seed + list(LANGUAGES).index(language) * 997)
    needed = target_counts(args.samples_per_language, args.task_ratios)
    counts = Counter()
    skipped = Counter()
    candidate_counts = Counter()
    selected: list[dict[str, Any]] = []
    source_pool: list[dict[str, Any]] = []
    used_file_hashes: set[str] = set()
    used_spans_by_file: dict[str, set[tuple[int, int]]] = defaultdict(set)
    token: str | bool | None = os.environ.get("HF_TOKEN") or True

    stream = load_stack_stream(language, token)
    scanned = 0
    kept_files = 0
    for source_index, row in enumerate(stream):
        if all(counts[task] >= needed[task] for task in TASK_TYPES):
            break
        scanned += 1
        if args.max_files_per_language and scanned > args.max_files_per_language:
            break
        raw_content = strip_bom(str(row.get("content") or "")).replace("\r\n", "\n").replace("\r", "\n")
        content = raw_content
        if args.strip_leading_comments:
            content, stripped_leading_comments = strip_leading_comments(raw_content, language)
            if stripped_leading_comments:
                skipped["stripped_leading_comments"] += 1
        row_for_filter = dict(row)
        row_for_filter["content"] = content
        row_for_filter["size"] = len(content)
        reason = file_filter_reason(row_for_filter, language, args)
        if reason:
            skipped[reason] += 1
            continue
        code_hash = stable_hash(normalize_code(content))
        if code_hash in used_file_hashes:
            skipped["duplicate_file"] += 1
            continue
        if code_hash in full_decontam:
            skipped["decontam_full_code"] += 1
            continue
        pathish = f"{get_row_repo(row)}/{get_row_path(row)}".lower()
        if any(key in pathish for key in ("safim", "human-eval", "humaneval", "benchmark")):
            skipped["benchmark_path"] += 1
            continue
        try:
            candidates = collect_candidates(content, language)
        except Exception as exc:
            skipped[f"candidate_exception:{type(exc).__name__}"] += 1
            continue
        if not candidates:
            skipped["no_candidates"] += 1
            continue
        for candidate in candidates:
            candidate_counts[candidate.task_type] += 1
        made = 0
        file_used = used_spans_by_file[code_hash]
        task_order = list(TASK_TYPES)
        rng.shuffle(task_order)
        for task_type in task_order:
            if counts[task_type] >= needed[task_type]:
                continue
            candidate = select_candidate(candidates, task_type, file_used, rng)
            if candidate is None:
                continue
            sample = make_sample(row, content, language, candidate, source_index)
            prompt_chars = len(sample["prefix"]) + len(sample["suffix"])
            prompt_limit = context_char_limit(language, task_type, args)
            if prompt_limit is not None and prompt_chars > prompt_limit:
                skipped[f"{task_type}:context_too_long"] += 1
                continue
            target_hash = stable_hash(normalize_code(sample["target"]))
            if len(sample["target"].strip()) >= 40 and target_hash in target_decontam:
                skipped[f"{task_type}:decontam_target"] += 1
                continue
            selected.append(sample)
            counts[task_type] += 1
            file_used.add((candidate.start, candidate.end))
            made += 1
        if made:
            kept_files += 1
            used_file_hashes.add(code_hash)
            source_pool.append(
                {
                    "source_dataset": "the_stack_v1",
                    "language": language,
                    "repo_name": get_row_repo(row),
                    "path": get_row_path(row),
                    "license": get_row_licenses(row),
                    "size": row.get("size") or len(content),
                    "content": content,
                    "raw_meta": source_meta(row),
                }
            )

    selected.sort(key=lambda x: (x["language"], x["task_type"], x["uid"]))
    report = {
        "language": language,
        "needed": dict(needed),
        "selected": dict(counts),
        "selected_total": len(selected),
        "scanned_files": scanned,
        "kept_source_files": kept_files,
        "skipped": dict(skipped),
        "candidate_counts": dict(candidate_counts),
    }
    missing = {task: needed[task] - counts[task] for task in TASK_TYPES if counts[task] < needed[task]}
    if missing:
        report["missing"] = missing
    return selected, source_pool, report


def parse_task_ratios(value: str) -> dict[str, float]:
    if not value:
        return dict(DEFAULT_TASK_RATIOS)
    ratios = dict(DEFAULT_TASK_RATIOS)
    for item in value.split(","):
        key, raw = item.split("=", 1)
        key = key.strip()
        if key not in TASK_TYPES:
            raise ValueError(f"unknown task type in --task-ratios: {key}")
        ratios[key] = float(raw)
    total = sum(ratios.values())
    if total <= 0:
        raise ValueError("task ratios must sum to a positive value")
    return {key: value / total for key, value in ratios.items()}


def prepare_viewer_data(out_dir: Path, viewer_dir: Path, test_dir: Path) -> dict[str, int]:
    viewer_dir.mkdir(parents=True, exist_ok=True)
    counts: dict[str, int] = {}
    for language in LANGUAGES:
        train_path = out_dir / f"safim_{language}_train_canonical.jsonl"
        train_rows = list(read_jsonl(train_path))
        counts[str(viewer_dir / f"train_safim_{language}.jsonl")] = write_jsonl(
            viewer_dir / f"train_safim_{language}.jsonl", train_rows
        )
        official_path = test_dir / f"safim_{language}.jsonl"
        if official_path.exists():
            official_rows = list(read_jsonl(official_path))
            counts[str(viewer_dir / f"official_safim_{language}.jsonl")] = write_jsonl(
                viewer_dir / f"official_safim_{language}.jsonl", official_rows
            )
    return counts


def main() -> None:
    parser = argparse.ArgumentParser(description="Build SAFIM-style multilingual FIM train data from The Stack v1 streaming source.")
    parser.add_argument("--out-dir", type=Path, default=Path("data/benchmark/train_data/safim"))
    parser.add_argument("--test-dir", type=Path, default=Path("data/benchmark/test_data"))
    parser.add_argument("--languages", nargs="+", choices=tuple(LANGUAGES), default=list(LANGUAGES))
    parser.add_argument("--samples-per-language", type=int, default=1000)
    parser.add_argument("--task-ratios", default="algorithmic_block=0.45,control_flow_expression=0.45,api_function_call=0.10")
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--max-files-per-language", type=int, default=200000)
    parser.add_argument("--min-file-chars", type=int, default=800)
    parser.add_argument("--max-file-chars", type=int, default=12000)
    parser.add_argument("--min-file-lines", type=int, default=20)
    parser.add_argument("--max-avg-line-length", type=float, default=160.0)
    parser.add_argument("--max-line-length", type=float, default=1000.0)
    parser.add_argument("--max-prompt-chars", type=int, default=0, help="Optional global context cap for len(prefix + suffix). 0 keeps only --length-profile caps.")
    parser.add_argument("--strip-leading-comments", action=argparse.BooleanOptionalAction, default=True, help="Strip only file-opening license/banner comments before filtering and slicing.")
    parser.add_argument("--length-profile", choices=("official_p95", "none"), default="official_p95")
    parser.add_argument("--allow-non-permissive-license", action="store_true")
    parser.add_argument("--prepare-viewer-data", action="store_true")
    parser.add_argument("--viewer-dir", type=Path, default=Path("outputs/benchmark/safim_stack_probe/viewer_data"))
    parser.add_argument("--no-strict-counts", action="store_true", help="Write partial output instead of failing if a bucket is underfilled.")
    args = parser.parse_args()
    args.task_ratios = parse_task_ratios(args.task_ratios)
    args.require_permissive_license = not args.allow_non_permissive_license

    full_decontam, target_decontam = load_decontam_hashes(args.test_dir)
    args.out_dir.mkdir(parents=True, exist_ok=True)

    all_rows: list[dict[str, Any]] = []
    reports: dict[str, Any] = {}
    written: dict[str, int] = {}
    for language in args.languages:
        rows, source_pool, report = build_language(language, args, full_decontam, target_decontam)
        reports[language] = report
        canonical_path = args.out_dir / f"safim_{language}_train_canonical.jsonl"
        chatml_path = args.out_dir / f"safim_{language}_train_chatml.jsonl"
        source_path = Path("data/raw_data/the_stack_v1/safim_source_pool") / f"{language}_sources.jsonl"
        written[str(canonical_path)] = write_jsonl(canonical_path, rows)
        written[str(chatml_path)] = write_jsonl(chatml_path, (make_chatml_row(row) for row in rows))
        written[str(source_path)] = write_jsonl(source_path, source_pool)
        all_rows.extend(rows)

    combined_canonical = args.out_dir / "safim_train_canonical.jsonl"
    combined_chatml = args.out_dir / "safim_train_chatml.jsonl"
    written[str(combined_canonical)] = write_jsonl(combined_canonical, all_rows)
    written[str(combined_chatml)] = write_jsonl(combined_chatml, (make_chatml_row(row) for row in all_rows))

    viewer_outputs = {}
    if args.prepare_viewer_data:
        viewer_outputs = prepare_viewer_data(args.out_dir, args.viewer_dir, args.test_dir)

    report = {
        "config": {
            "source": "bigcode/the-stack",
            "mode": "streaming",
            "languages": args.languages,
            "samples_per_language": args.samples_per_language,
            "task_ratios": args.task_ratios,
            "target_counts_per_language": target_counts(args.samples_per_language, args.task_ratios),
            "seed": args.seed,
            "max_files_per_language": args.max_files_per_language,
            "min_file_chars": args.min_file_chars,
            "max_file_chars": args.max_file_chars,
            "max_prompt_chars": args.max_prompt_chars,
            "strip_leading_comments": args.strip_leading_comments,
            "length_profile": args.length_profile,
            "official_context_p95_chars": OFFICIAL_CONTEXT_P95_CHARS if args.length_profile == "official_p95" else None,
            "require_permissive_license": args.require_permissive_license,
            "test_dir": str(args.test_dir),
        },
        "languages": reports,
        "written": written,
        "viewer_outputs": viewer_outputs,
        "total_rows": len(all_rows),
    }
    report_path = args.out_dir / "build_report.json"
    write_json(report_path, report)

    if not args.no_strict_counts:
        missing = {
            lang: data.get("missing")
            for lang, data in reports.items()
            if data.get("missing")
        }
        if missing:
            print(json.dumps(report, ensure_ascii=False, indent=2))
            raise SystemExit(f"underfilled SAFIM train buckets: {missing}")

    print(json.dumps({"report": str(report_path), "total_rows": len(all_rows), "written": written, "viewer_outputs": viewer_outputs}, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
