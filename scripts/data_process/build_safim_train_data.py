def get_row_licenses(row: dict[str, Any]) -> list[str]:
    values: list[str] = []
    for key in ("max_stars_repo_licenses", "max_issues_repo_licenses", "max_forks_repo_licenses", "licenses", "license"):
        value = row.get(key)
        if not value:
            continue
        if isinstance(value, str):
            values.append(value)
        elif isinstance(value, list):
            values.extend(str(x) for x in value if x)
    return sorted(set(values))


def has_permissive_license(row: dict[str, Any]) -> bool:
    licenses = get_row_licenses(row)
    if not licenses:
        return False
    joined = " ".join(licenses).lower()
    return any(hint in joined for hint in PERMISSIVE_LICENSE_HINTS)


def extension_from_path(path: str, fallback: str = "") -> str:
    suffix = Path(path).suffix.lower().lstrip(".")
    return suffix or fallback.lower().lstrip(".")


def strip_bom(text: str) -> str:
    return text.lstrip("\ufeff")


def _consume_block_comment(text: str, pos: int) -> int | None:
    if not text.startswith("/*", pos):
        return None
    end = text.find("*/", pos + 2)
    if end < 0:
        return None
    end += 2
    while end < len(text) and text[end] in " \t":
        end += 1
    if end < len(text) and text[end] == "\n":
        end += 1
    return end


def _consume_triple_string(text: str, pos: int) -> int | None:
    quote = None
    for candidate in ('"""', "'''"):
        if text.startswith(candidate, pos):
            quote = candidate
            break
    if quote is None:
        return None
    end = text.find(quote, pos + 3)
    if end < 0:
        return None
    end += 3
    while end < len(text) and text[end] in " \t":
        end += 1
    if end < len(text) and text[end] == "\n":
        end += 1
    return end


def strip_leading_comments(content: str, language: str) -> tuple[str, bool]:
    """Remove only file-opening comment/banner material before real code."""
    original = content
    text = strip_bom(content).replace("\r\n", "\n").replace("\r", "\n")
    pos = 0
    changed = text != original

    while True:
        before = pos
        while pos < len(text) and text[pos] in " \t\n":
            pos += 1

        if language == "python":
            line_end = text.find("\n", pos)
            line_end = len(text) if line_end < 0 else line_end
            stripped = text[pos:line_end].strip()
            if stripped.startswith("#!") or re.match(r"#.*coding[:=]", stripped):
                pos = min(line_end + 1, len(text))
                changed = True
                continue
            if stripped.startswith("#"):
                while pos < len(text):
                    line_end = text.find("\n", pos)
                    line_end = len(text) if line_end < 0 else line_end
                    stripped = text[pos:line_end].strip()
                    if stripped.startswith("#") or not stripped:
                        pos = min(line_end + 1, len(text))
                    else:
                        break
                changed = True
                continue
            triple_end = _consume_triple_string(text, pos)
            if triple_end is not None:
                pos = triple_end
                changed = True
                continue
        else:
            block_end = _consume_block_comment(text, pos)
            if block_end is not None:
                pos = block_end
                changed = True
                continue
            line_start = pos
            while pos < len(text):
                line_end = text.find("\n", pos)
                line_end = len(text) if line_end < 0 else line_end
                stripped = text[pos:line_end].strip()
                if stripped.startswith("//") or stripped.startswith("///") or not stripped:
                    pos = min(line_end + 1, len(text))
                else:
                    break
            if pos != line_start:
                changed = True
                continue

        if pos == before:
            break
        break

    return text[pos:], changed or pos > 0


def is_comment_only_or_empty(target: str) -> bool:
    stripped = target.strip()
    if not stripped:
        return True
    non_comment = []
    for line in stripped.splitlines():
        s = line.strip()
        if not s:
            continue
        if s.startswith(("#", "//", "/*", "*", "*/")):
            continue
        non_comment.append(s)
    return not non_comment


def is_low_value_target(target: str) -> bool:
    stripped = target.strip()
    if is_comment_only_or_empty(stripped):
        return True
    if stripped in {"pass", "break", "continue", "return", "return;", "return null;", "return None", "return true;", "return false;"}:
        return True
    if re.fullmatch(r"[{};]+", stripped):
        return True
    return False


def passes_target_length(task_type: str, target: str) -> bool:
    stripped = target.strip("\n")
    chars = len(stripped.strip())
    lines = sum(1 for line in stripped.splitlines() if line.strip()) or 1
    if task_type == "algorithmic_block":
        return 5 <= chars <= 300 and 1 <= lines <= 5
    if task_type == "control_flow_expression":
        return 3 <= chars <= 120 and lines == 1
    if task_type == "api_function_call":
        return 5 <= chars <= 220 and lines <= 3
    return False


def make_candidate(task_type: str, start: int, end: int, kind: str, score: tuple[int, int, int], content: str) -> SpanCandidate | None:
    if start < 0 or end <= start or end > len(content):
        return None
    target = content[start:end]
    if not passes_target_length(task_type, target):
        return None
    if is_low_value_target(target):
        return None
    return SpanCandidate(task_type, start, end, kind, score)


def file_filter_reason(row: dict[str, Any], language: str, args: argparse.Namespace) -> str | None:
    content = strip_bom(str(row.get("content") or "")).replace("\r\n", "\n").replace("\r", "\n")
    if len(content) < args.min_file_chars:
        return "too_short_file"
    if len(content) > args.max_file_chars:
        return "too_long_file"
    if content.count("\n") + 1 < args.min_file_lines:
        return "too_few_lines"
    if row.get("avg_line_length") and float(row["avg_line_length"]) > args.max_avg_line_length:
        return "avg_line_too_long"
    if row.get("max_line_length") and float(row["max_line_length"]) > args.max_line_length:
        return "max_line_too_long"
    head = content[:5000].lower()
    if any(marker in head for marker in GENERATED_MARKERS):
        return "generated_marker"
    path = get_row_path(row)
    repo = get_row_repo(row)
    pathish = f"{repo}/{path}"
    if BAD_PATH_RE.search(pathish):
        return "bad_path"
    if CONFIG_PATH_RE.search(pathish):
        return "config_path"
    ext = extension_from_path(path, str(row.get("ext") or ""))
    allowed = LANGUAGES[language]["exts"]
    if ext and ext not in allowed:
        return "bad_extension"
    if language == "cpp" and ext in {"h", "hh", "hpp", "hxx"}:
        if content.count("{") < 3 or not re.search(r"\)\s*(?:const\s*)?(?:noexcept\s*)?(?:->\s*[\w:<>,\s*&]+\s*)?\{", content):
            return "cpp_header_without_bodies"
    if language == "java":
        if re.search(r"\binterface\s+\w+", content) and content.count("{") <= 2:
            return "java_interface_only"
        if content.count(";") > 0 and content.count("if (") + content.count("for (") + content.count("while (") == 0 and content.count("return ") <= 1:
            return "java_pojo_like"
    if language == "csharp":
        if content.count("=>") + content.count(" get;") + content.count(" set;") > 8 and content.count("if (") + content.count("foreach (") + content.count("while (") == 0:
            return "csharp_model_like"
    if language == "python":
        if path.endswith("setup.py") or content.count("def ") + content.count("class ") == 0 and content.count("if ") < 2:
            return "python_config_or_no_logic"
    if args.require_permissive_license and not has_permissive_license(row):
        return "non_permissive_or_unknown_license"
    return None


def py_node_offsets(content: str, node: ast.AST, offsets: list[int]) -> tuple[int, int] | None:
    if not all(hasattr(node, attr) for attr in ("lineno", "col_offset", "end_lineno", "end_col_offset")):
        return None
    return line_col_to_offset(offsets, node.lineno, node.col_offset), line_col_to_offset(offsets, node.end_lineno, node.end_col_offset)


def collect_python_candidates(content: str) -> list[SpanCandidate]:
    try:
        tree = ast.parse(content)
    except SyntaxError:
        return []
    offsets = line_offsets(content)
    candidates: list[SpanCandidate] = []

    for node in ast.walk(tree):
        if isinstance(node, (ast.If, ast.While)):
            span = py_node_offsets(content, node.test, offsets)
            if span:
                c = make_candidate("control_flow_expression", span[0], span[1], type(node).__name__ + ".test", (4, span[1] - span[0], -span[0]), content)
                if c:
                    candidates.append(c)
        elif isinstance(node, (ast.For, ast.AsyncFor)):
            span = py_node_offsets(content, node.iter, offsets)
            if span:
                c = make_candidate("control_flow_expression", span[0], span[1], type(node).__name__ + ".iter", (3, span[1] - span[0], -span[0]), content)
                if c:
                    candidates.append(c)
        elif isinstance(node, ast.comprehension):
            span = py_node_offsets(content, node.iter, offsets)
            if span:
                c = make_candidate("control_flow_expression", span[0], span[1], "comprehension.iter", (1, span[1] - span[0], -span[0]), content)
                if c:
                    candidates.append(c)
            for cond in node.ifs:
                span = py_node_offsets(content, cond, offsets)
                if span:
                    c = make_candidate("control_flow_expression", span[0], span[1], "comprehension.if", (1, span[1] - span[0], -span[0]), content)
                    if c:
                        candidates.append(c)
        elif isinstance(node, ast.Call):
            span = py_node_offsets(content, node, offsets)
            if not span:
                continue
            target = content[span[0] : span[1]]
            callee = target.split("(", 1)[0].split(".")[-1].strip()
            if callee in LOW_VALUE_CALLS:
                continue
            score = (4 if "." in target.split("(", 1)[0] else 2, len(target), -span[0])
            c = make_candidate("api_function_call", span[0], span[1], "Call", score, content)
            if c:
                candidates.append(c)

    for parent in ast.walk(tree):
        bodies: list[list[ast.stmt]] = []
        for attr in ("body", "orelse", "finalbody"):
            body = getattr(parent, attr, None)
            if isinstance(body, list):
                bodies.append([x for x in body if isinstance(x, ast.stmt)])
        handlers = getattr(parent, "handlers", None)
        if isinstance(handlers, list):
            for handler in handlers:
                body = getattr(handler, "body", None)
                if isinstance(body, list):
                    bodies.append([x for x in body if isinstance(x, ast.stmt)])
        for body in bodies:
            if not body:
                continue
            for width in range(1, min(5, len(body)) + 1):
                for idx in range(0, len(body) - width + 1):
                    group = body[idx : idx + width]
                    start_line = group[0].lineno
                    end_line = group[-1].end_lineno
                    start, end = span_from_lines(content, start_line, end_line)
                    target = content[start:end]
                    interesting = sum(isinstance(x, (ast.Assign, ast.AnnAssign, ast.AugAssign, ast.Return, ast.Expr, ast.Break, ast.Continue)) for x in group)
                    score = (interesting, width, -start)
                    c = make_candidate("algorithmic_block", start, end, "statement_group", score, content)
                    if c:
                        candidates.append(c)
                if width >= 3:
                    break
    return dedup_candidates(candidates)


def find_matching_paren(text: str, open_idx: int) -> int | None:
    depth = 0
    quote: str | None = None
    escape = False
    for idx in range(open_idx, len(text)):
        ch = text[idx]
        if quote:
            if escape:
                escape = False
            elif ch == "\\":
                escape = True
            elif ch == quote:
                quote = None
            continue
        if ch in {'"', "'"}:
            quote = ch
            continue
        if ch == "(":
            depth += 1
        elif ch == ")":
            depth -= 1
            if depth == 0:
                return idx
    return None


def expression_before_paren(text: str, open_idx: int) -> tuple[int, str] | None:
    idx = open_idx - 1
    while idx >= 0 and text[idx].isspace():
        idx -= 1
    end = idx + 1
    allowed = set("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789_.$:<>~")
    while idx >= 0 and text[idx] in allowed:
        idx -= 1
    start = idx + 1
    expr = text[start:end].strip()
    if not expr:
        return None
    return start, expr


def iter_control_parens(content: str) -> Iterator[tuple[int, int, str]]:
    for match in re.finditer(r"\b(if|while|for|switch|catch)\s*\(", content):
        open_idx = content.find("(", match.start())
        close_idx = find_matching_paren(content, open_idx)
        if close_idx is None:
            continue
        yield open_idx + 1, close_idx, match.group(1)


def iter_call_spans(content: str) -> Iterator[tuple[int, int, str]]:
    for match in re.finditer(r"\(", content):
        open_idx = match.start()
        before = expression_before_paren(content, open_idx)
        if not before:
            continue
        start, callee = before
        base = callee.split(".")[-1].split("::")[-1].split("->")[-1].strip("~")
        if not base or base in CONTROL_KEYWORDS or base in LOW_VALUE_CALLS:
            continue
        # Skip declarations like function foo(int x) by requiring either a receiver/namespace or assignment/call context.
        prefix = content[max(0, start - 20) : start]
        if "." not in callee and "::" not in callee and "->" not in callee and "new " not in prefix and "=" not in prefix and "return" not in prefix:
            continue
        close_idx = find_matching_paren(content, open_idx)
        if close_idx is None:
            continue
        yield start, close_idx + 1, "call_expression"


def collect_clike_block_candidates(content: str) -> list[SpanCandidate]:
    lines = content.split("\n")
    candidates: list[SpanCandidate] = []

    interesting_lines: list[tuple[int, int]] = []
    for line_no, line in enumerate(lines, start=1):
        stripped = line.strip()
        if not stripped or stripped.startswith(("//", "/*", "*", "#")):
            continue
        if stripped in {"{", "}", "};", "};"}:
            continue
        if any(k in stripped for k in ("=", "+=", "-=", "*=", "/=", "return", ".Add(", ".add(", ".push", ".append", "throw ")):
            if stripped.endswith((";", "}", ")")) or "return" in stripped:
                start, end = span_from_lines(content, line_no, line_no)
                interesting_lines.append((line_no, end - start))

    # Single lines and short contiguous groups are useful for SAFIM block_completion.
    max_idx = len(interesting_lines)
    for idx in range(max_idx):
        for width in range(1, min(5, max_idx - idx) + 1):
            group = interesting_lines[idx : idx + width]
            line_nums = [x[0] for x in group]
            if line_nums[-1] - line_nums[0] + 1 != width:
                break
            start, end = span_from_lines(content, line_nums[0], line_nums[-1])
            target = content[start:end]
            c = make_candidate("algorithmic_block", start, end, "statement_group", (width, len(target), -start), content)
            if c:
                candidates.append(c)

    # Prefer compact bodies immediately inside control braces.
    for match in re.finditer(r"\b(if|else|for|while|switch|case|try|catch)\b[^\n{;]*\{", content):
        brace = content.find("{", match.start())
        depth = 0
        end_brace = None
        for pos in range(brace, min(len(content), brace + 2000)):
            if content[pos] == "{":
                depth += 1
            elif content[pos] == "}":
                depth -= 1
                if depth == 0:
                    end_brace = pos
                    break
        if end_brace is None:
            continue
        start = brace + 1
        end = end_brace
        target = content[start:end]
        nonblank = [line for line in target.splitlines() if line.strip()]
        if 1 <= len(nonblank) <= 5:
            # Keep inner indentation and trailing newline when available.
            c = make_candidate("algorithmic_block", start, end, match.group(1) + "_body", (5, len(nonblank), -start), content)
            if c:
                candidates.append(c)
    return candidates


def collect_clike_candidates(content: str) -> list[SpanCandidate]:
    candidates: list[SpanCandidate] = []
    for start, end, keyword in iter_control_parens(content):
        c = make_candidate("control_flow_expression", start, end, keyword + "_condition", (4, end - start, -start), content)
        if c:
            candidates.append(c)
    for start, end, kind in iter_call_spans(content):
        target = content[start:end]
        score = (4 if any(x in target.split("(", 1)[0] for x in (".", "::", "->")) else 2, len(target), -start)
        c = make_candidate("api_function_call", start, end, kind, score, content)
        if c:
            candidates.append(c)
    candidates.extend(collect_clike_block_candidates(content))
    return dedup_candidates(candidates)


def dedup_candidates(candidates: list[SpanCandidate]) -> list[SpanCandidate]:
    best: dict[tuple[str, int, int], SpanCandidate] = {}
    for candidate in candidates:
        key = (candidate.task_type, candidate.start, candidate.end)
        old = best.get(key)
        if old is None or candidate.score > old.score:
            best[key] = candidate
    return list(best.values())


def collect_candidates(content: str, language: str) -> list[SpanCandidate]:
    if language == "python":
        return collect_python_candidates(content)
    return collect_clike_candidates(content)


def select_candidate(candidates: list[SpanCandidate], task_type: str, used: set[tuple[int, int]], rng: random.Random) -> SpanCandidate | None:
    filtered = [c for c in candidates if c.task_type == task_type and (c.start, c.end) not in used]
    if not filtered:
        return None
    filtered.sort(key=lambda c: c.score, reverse=True)
    top = filtered[: min(20, len(filtered))]
    return rng.choice(top)


def target_counts(samples_per_language: int, ratios: dict[str, float]) -> dict[str, int]:
    raw = {task: samples_per_language * ratios[task] for task in TASK_TYPES}
    counts = {task: int(raw[task]) for task in TASK_TYPES}
    remainder = samples_per_language - sum(counts.values())
    order = sorted(TASK_TYPES, key=lambda t: raw[t] - counts[t], reverse=True)
    for task in order[:remainder]:
        counts[task] += 1
    return counts


def context_char_limit(language: str, task_type: str, args: argparse.Namespace) -> int | None:
    limits: list[int] = []
    if args.length_profile == "official_p95":
        limits.append(OFFICIAL_CONTEXT_P95_CHARS[language][task_type])
    if args.max_prompt_chars and args.max_prompt_chars > 0:
        limits.append(args.max_prompt_chars)
    if not limits:
        return None
    return min(limits)


def source_meta(row: dict[str, Any]) -> dict[str, Any]:
    keys = [
        "hexsha",
        "size",
        "ext",
        "lang",
        "avg_line_length",
        "max_line_length",
        "alphanum_fraction",
        "max_stars_repo_path",
        "max_stars_repo_name",
        "max_stars_repo_licenses",
        "max_issues_repo_path",
        "max_issues_repo_name",
        "max_issues_repo_licenses",
        "max_forks_repo_path",
        "max_forks_repo_name",
        "max_forks_repo_licenses",
    ]
    return {key: row.get(key) for key in keys if key in row}


def load_decontam_hashes(test_dir: Path) -> tuple[set[str], set[str]]:
    full_hashes: set[str] = set()
    target_hashes: set[str] = set()
    for path in test_dir.glob("safim_*.jsonl"):
        for row in read_jsonl(path):
            full_code = str(row.get("prefix", "")) + str(row.get("target", "")) + str(row.get("suffix", ""))
            target = str(row.get("target", ""))
            if full_code.strip():
                full_hashes.add(stable_hash(normalize_code(full_code)))
            if len(target.strip()) >= 40:
                target_hashes.add(stable_hash(normalize_code(target)))
    return full_hashes, target_hashes


def make_sample(row: dict[str, Any], content: str, language: str, candidate: SpanCandidate, source_index: int) -> dict[str, Any]:
    prefix, target, suffix = slice_span(content, candidate.start, candidate.end)
    if prefix + target + suffix != content:
        raise ValueError("internal span slicing error")
    path = get_row_path(row)
    repo = get_row_repo(row)
    uid_seed = f"{language}:{candidate.task_type}:{repo}:{path}:{candidate.start}:{candidate.end}:{stable_hash(content)}:{source_index}"
    uid = f"safim_train:{language}:{candidate.task_type}:{stable_hash(uid_seed)[:16]}"
    return {
        "uid": uid,
        "benchmark": "safim",
        "source": "derived_train",
        "source_dataset": "the_stack_v1",
        "language": language,
        "task_type": candidate.task_type,
        "official_task_id": uid,
        "entry_point": None,
        "prefix": prefix,
        "target": target,
        "suffix": suffix,
        "full_code": content,
        "target_kind": candidate.target_kind,
        "metadata": {
            "source_dataset": "the_stack_v1",
            "repo_name": repo,
            "path": path,
            "licenses": get_row_licenses(row),
            "target_start": candidate.start,
            "target_end": candidate.end,
            "target_chars": len(target),
            "target_lines": sum(1 for line in target.splitlines() if line.strip()),
            "raw_meta": source_meta(row),
        },
    }


